    // Boton nueva entrada sala central
    private void btnCentralNuevaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCentralNuevaActionPerformed
        // Verificamos que tenga pelicula la sala
        if(checarSala(this._central)){
            // ingresamos un cliente
            if(this._central.entraUno()){
                JOptionPane.showMessageDialog(null, "Entrada registrada");
            } else {
                JOptionPane.showMessageDialog(null, "Sala llena", null, JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Mostramos error
            JOptionPane.showMessageDialog(null, "Debe registrar una película primero", null, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCentralNuevaActionPerformed


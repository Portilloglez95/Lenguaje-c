    // Verificamos si la sala tiene asignada una pelicula
    private boolean checarSala(SalaCine temp)
    {
        // Verificamos
        return !temp.getPelicula().isEmpty();
    }
    
    // Boton cambiar pelicula sala central
    private void btnCentralCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCentralCambiarActionPerformed
        // Variables a utilizar
        String titulo = "";
        
        // Obtenemos el titulo de la pelicula
        titulo = JOptionPane.showInputDialog(null, "Ingrese el titulo de la película: ");
        
        // Verificamos que se ingreso un titulo
        if(!titulo.isEmpty()){
            // Formateaamos el titulo
            titulo = titulo.toUpperCase();
            
            // Cambiamos el titulo de la pelicula
            this._central.setPelicula(titulo);
            
            // Mostramos la nueva pelicula
            lblCentralPelicula.setText(titulo);
        }
    }//GEN-LAST:event_btnCentralCambiarActionPerformed
    

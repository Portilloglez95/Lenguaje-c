    // Boton % ocupacion sala central
    private void btnCentralOcupacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCentralOcupacionActionPerformed
        // Variable a utilizar
        String info;
        
        // Verificamos que tenga pelicula la sala
        if(checarSala(this._central)){
            // Configuracion de la informacion a mostrar
            info = "Pelicula: "+this._central.getPelicula()+"\n";
            info += "Aforo total: "+this._central.getAforo()+"\n";
            info += "Asientos ocupados: "+this._central.getOcupadas()+"\n";
            info += "Asientos libres: "+this._central.getLibres()+"\n";
            info += "Ocupación: "+this._central.getPorcentajes()+"%";
            
            // Mostramos la informacion
            JOptionPane.showMessageDialog(null, info);
        } else {
            // Mostramos error
            JOptionPane.showMessageDialog(null, "Debe registrar una película primero", null, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCentralOcupacionActionPerformed

